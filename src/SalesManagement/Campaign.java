    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        
        String campaignName = txtCampaignName.getText();
        String budget = txtBudget.getText();
        String type = txtType.getSelectedItem().toString();
        
        
        try {
            pst = con.prepareStatement("INSERT INTO campaign_table (campaignName,budget,type) VALUES(?,?,?)");
            pst.setString(1, campaignName);
            pst.setString(2, budget);
            pst.setString(3, type);
            int result = pst.executeUpdate();
            
            if(result == 1)
            {
                JOptionPane.showMessageDialog(this, "Campaign Added Successfully");
                txtCampaignName.setText("");
                txtBudget.setText("");
                txtType.setSelectedIndex(-1);
                txtCampaignName.requestFocus();
                campaignList();
            
            }
            else
            {
            JOptionPane.showMessageDialog(this, " An error occurred!");
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Campaign.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addButtonActionPerformed


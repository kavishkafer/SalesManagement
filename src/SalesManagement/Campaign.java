    public void campaignList()
    {
        int count;
        try {
            pst = con.prepareStatement("SELECT * FROM campaign_table");
            rs = pst.executeQuery();
            ResultSetMetaData rsd = rs.getMetaData();
            count = rsd.getColumnCount();
            
            DefaultTableModel d = (DefaultTableModel)campaignListTable.getModel();
            d.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                
                for (int i = 1; i <= count; i++) 
                {
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("campaignName"));
                    v2.add(rs.getString("budget"));
                    v2.add(rs.getString("type"));
                    
                }
                d.addRow(v2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Campaign.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
    
    

